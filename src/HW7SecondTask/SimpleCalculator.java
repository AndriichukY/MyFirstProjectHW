package HW7SecondTask;

public class SimpleCalculator {

    public double add(double x, double y) {
        return x + y;
    }

    public double sub(double x, double y) {
        return x - y;
    }

    public double multiply(double x, double y) {
        return x * y;
    }

    public double div(double x, double y) {
        return x / y;
    }
}
